public class ForLoopArrays {
    
public static void main(String[] args) {
    int [] a={32,4,5,5,};
     for(int i = 0;i<a.length; i++)
     {
        System.out.println(a[i]);
     }                                                                
     System.out.println("------------------------------------");
     for(int i:a){                 
        System.out.println(i);
     }                              // Foreach loop use 
}





}
